function getRegion(regionid) {
	if (regionid == null || "" == regionid || "undefind" == regionid) {
		return;
	}
	regionid = "" + regionid;
	var pro = regionid.substr(0, 2);
	var city = regionid.substr(2, 2);
	var pro_obj = null;
	var city_obj = null;
	var area_obj = null;
	var str = "";
	for ( var i = 0; i < region.length; i++) {
		if (("" + region[i].id).substr(0, 2) == pro) {
			pro_obj = region[i];
			str += pro_obj.name;
			break;
		}
	}
	if (pro_obj) {
		for ( var j = 0; j < pro_obj.children.length; j++) {
			if (("" + pro_obj.children[j].id).substr(0, 4) == (pro + city)) {
				city_obj = pro_obj.children[j];
				str += "-";
				str += city_obj.name;
				break;
			}
		}
	}
	if (city_obj) {
		for ( var k = 0; k < city_obj.children.length; k++) {
			if (("" + city_obj.children[k].id) == regionid) {
				area_obj = city_obj.children[k];
				str += "-";
				str += area_obj.name;
				break;
			}
		}
	}
	return str;
};
