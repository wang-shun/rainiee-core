package com.dimeng.p2p;

/**
 * 上传文件类型.
 */
public enum FileType {

	/**
	 * 文章封面图片
	 */
	ARTICLE_IMAGE,
	/**
	 * 文章附件
	 */
	ARTICLE_ATTACHMENT,
	/**
	 * 广告图片类型
	 */
	ADVERTISEMENT_IMAGE,
	/**
	 * 客服图片类型
	 */
	CUSTOMER_SERVICE_IMAGE,
	/**
	 * 合作伙伴图片类型
	 */
	PARTNER_IMAGE,
	/**
	 * 业绩报告附件
	 */
	PERFORMANCE_REPORT_ATTACHMENT,
	/**
	 * 用户头像类型
	 */
	USER_IMAGE,
	/**
	 * 背景图片类型
	 */
	BACKGROUND_IMAGE;
}
